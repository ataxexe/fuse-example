package com.ibm.coh.avustus.data;

public class SportType {

	private String description;
	private Integer men;
	private Integer women;
	private Integer boys;
	private Integer girls;
	private Double adults;
	private Double young;
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public Integer getMen() {
		return men;
	}
	public void setMen(Integer men) {
		this.men = men;
	}
	public Integer getWomen() {
		return women;
	}
	public void setWomen(Integer women) {
		this.women = women;
	}
	public Integer getBoys() {
		return boys;
	}
	public void setBoys(Integer boys) {
		this.boys = boys;
	}
	public Integer getGirls() {
		return girls;
	}
	public void setGirls(Integer girls) {
		this.girls = girls;
	}
	public Double getAdults() {
		return adults;
	}
	public void setAdults(Double adults) {
		this.adults = adults;
	}
	public Double getYoung() {
		return young;
	}
	public void setYoung(Double young) {
		this.young = young;
	}
	
	
	
	
}
